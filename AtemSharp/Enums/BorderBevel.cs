namespace AtemSharp.Enums;

/// <summary>
/// Border bevel effect types for DVE
/// </summary>
public enum BorderBevel
{
    /// <summary>
    /// No bevel effect
    /// </summary>
    None = 0,
    
    /// <summary>
    /// Inward and outward bevel effect
    /// </summary>
    InOut = 1,
    
    /// <summary>
    /// Inward bevel effect only
    /// </summary>
    In = 2,
    
    /// <summary>
    /// Outward bevel effect only
    /// </summary>
    Out = 3
}